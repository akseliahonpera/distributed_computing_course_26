    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {
        if (patient == null) {
            JOptionPane.showMessageDialog(this,
                    "Please select a patient to delete.",
                    "No Selection",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (patient.getId() == null) {
            JOptionPane.showMessageDialog(this,
                    "Cannot delete: patient ID is missing!",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Are you sure you want to delete patient " + patient.getFName() + " " + patient.getLName() + "?",
                "Confirm Deletion",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                Result<Void> result = PatientService.getInstance().deletePatient(patient);
                if (result.isSuccess()) {
                    JOptionPane.showMessageDialog(this,
                            "Patient deleted successfully.",
                            "Deleted",
                            JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Failed to delete patient!",
                            "Error",
                            JOptionPane.ERROR_MESSAGE);
                    return;
                }

                model.deletePatient(patient);
                patient = null;

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,
                        "Error deleting patient: " + e.getMessage(),
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
    }// GEN-LAST:event_jButton4ActionPerformed

