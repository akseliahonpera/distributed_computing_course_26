    public void passQueryResults(Patient[] patientlist) {
        if (patientlist == null) {
            model.setPatients(new ArrayList<>());
            return;
        }
        List<Patient> list = new ArrayList<>(Arrays.asList(patientlist));
        model.setPatients(list);
    }

